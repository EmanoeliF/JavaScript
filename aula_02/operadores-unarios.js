// Operador unário de negação (-)
let numero = 5;
let negativo = -numero; // Converte o número para o valor negativo
console.log(negativo); // Saída: -5

// Operador unário de incremento (++)
let contador = 0;
contador++; // Incrementa o valor de 'contador' em 1
console.log(contador); // Saída: 1

// Operador unário de tipo (typeof)
let texto = "Olá, mundo!";
console.log(typeof texto); // Saída: "string"
