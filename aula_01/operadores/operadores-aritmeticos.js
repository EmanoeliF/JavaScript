// Adição (+) Soma dois valores.
let a = 5;
let b = 3;
console.log(a + b); // 8

//Subtração (-) Subtrai um valor do outro.
console.log(a - b); // 2

// Multiplicação (*) multiplica dois valores.
console.log(a * b); // 15

//Divisão (/) Divide um valor pelo outro.
console.log(a / b); // 1.6666666666666667

// Resto da divisão (%) retorna o resto da divisão entre dois valores.
console.log(a % b); // 2

// Operadores de incremento e decremento:
//Incremento (++) Incrementa o valor em 1.
let c = 10;
c++;
console.log(c); // 11

//Decremento (--) Decrementa o valor em 1.
c--;
console.log(c); // 10

//Exponenciação (**): Eleva um número a uma potência.
let base = 2;
let expoente = 3;
console.log(base ** expoente); // 8

// Operadores de atribuição combinados: Esses operadores combinam a operação aritmética com a atribuição.
// Adição e atribuição (+=)
let x = 5;
x += 3; // x = x + 3
console.log(x); // 8

//Subtração e atribuição (-=)
x -= 2; // x = x - 2
console.log(x); // 6

//Multiplicação e atribuição (*=)
x *= 2; // x = x * 2
console.log(x); // 12

//Divisão e atribuição (/=)
x /= 4; // x = x / 4
console.log(x); // 3

//Resto e atribuição (%=)
x %= 2; // x = x % 2
console.log(x); // 1

